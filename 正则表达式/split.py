# 用正则表达式切分字符串比用固定的字符更灵活，
# 请看正常的切分代码
s = '1 3 4 5  6'
v = s.split(' ')
# 无法识别连续的空格
print(v) #['1', '3', '4', '5', '', '6']

import re
r = r'\s+'
print(re.split(r,s))#['1', '3', '4', '5', '6']

# 无论多少个空格都可以正常分割。加入,试试
print(re.split(r'[\s\,]+','a,b,c c'))#['a', 'b', 'c', 'c']
print(re.split(r'[\s\,]+', 'a,b, c  d'))#['a', 'b', 'c', 'd']

# 再加入;试试
print(re.split(r'[\s\,\;]+', 'a,b;; c  d'))#['a', 'b', 'c', 'd']